int	ft_sqrt(int nb)
{
	int	i;
	int	n;

	n = nb;
	i = 2;
	while (n == 1 || i == 0)
		return (n);
	while (i * i <= n)
	{
		if (n == i * i)
			return (i);
		i++;
	}
	return (0);
}
