int	ft_is_prime(int nb)
{
	int	n;

	n = 2;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (0);
	while (n >= nb)
	{
		if (nb % n == 0)
		{
			return (0);
		}
		else
			n++;
	}
	return (1);
}
